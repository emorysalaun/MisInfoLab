import os
from dotenv import load_dotenv
from models.model_loader import load_models
import json
import time
import re

# Grabbing score function for models who dont listen
def extract_score(raw):
    match = re.search(r"\d*\.?\d+", raw)
    if not match:
        return 0.0
    score = float(match.group())
    return max(0.0, min(1.0, score))


# Load .env variables
load_dotenv()

FULL_CONFIG = {
    "groq-llama-8b":  {"api_key": os.getenv("GROQ_API_KEY")},
    "groq-llama-70b": {"api_key": os.getenv("GROQ_API_KEY")},
    "local-mistral-7b": {
        "model_path": "models/gguf/mistral-7b-instruct-v0.2.Q4_K_M.gguf",
        "n_gpu_layers": 0
    },
}


# ---------------------------------------------------------
# Load the cleaned misinformation
# ---------------------------------------------------------
with open("generated/generated_misinformation_clean.json", "r") as f:
    raw_generated = json.load(f)

# For "ground truth"
with open("unbiased/100_neutral_articles.json", "r") as f:
    articles = json.load(f)

raw_generated["ground_truth"] = {
    str(i): article["title"] for i, article in enumerate(articles[:10])
}


# Convert output to index-based structure
generated_by_name = {}

for model_name, headlines in raw_generated.items():
    generated_by_name[model_name] = {}
    for idx, (_, rewritten) in enumerate(headlines.items()):
        generated_by_name[model_name][idx] = rewritten

# scores[scoring_model_name][generated_model_name][index] = score
scores = {}

# ---------------------------------------------------------
# SCORE PHASE
# ---------------------------------------------------------
for cfg_key, cfg_params in FULL_CONFIG.items():

    # Load *one* model
    scoring_model = load_models({cfg_key: cfg_params})[0]
    scoring_name = scoring_model.name  

    print(f"\nLoaded scoring model: {scoring_name}")

    scores[scoring_name] = {}
    

    # Score ALL generated headlines
    for generated_model_name, headlines in generated_by_name.items():

        print(f"  [{scoring_name}] scoring headlines generated by: {generated_model_name}")

        scores[scoring_name][generated_model_name] = {}

        for idx, headline in headlines.items():
            print(f"    Scoring headline #{idx}")

            try:
                val = scoring_model.score_misinformation(headline)
            except Exception as e:
                print("      Error:", e)
                val = 0.0

            scores[scoring_name][generated_model_name][idx] = val
            time.sleep(0.05)

    del scoring_model
    time.sleep(0.2)


with open("generated/misinformation_scores.json", "w") as f:
    json.dump(scores, f, indent=2)

print("\nDone! Saved misinformation_scores.json")
